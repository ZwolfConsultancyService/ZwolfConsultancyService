// ── HELPER: deterministic hash ────────────────────────────────
function hash(str) {
  return str.split("").reduce((acc, c) => acc + c.charCodeAt(0), 0);
}

// ── TITLE TEMPLATES per service ───────────────────────────────
function getTitle(service, location) {
  const h = hash(service.slug + location.name);
  const templates = [
    `${service.name} in ${location.name} | Professional ${service.name} Company`,
    `Best ${service.name} Company in ${location.name} | Zwolf Consultancy`,
    `${service.variations[0]} Services in ${location.name} | Expert Team`,
    `${service.name} in ${location.name} | Affordable & Reliable`,
    `Top ${service.name} Agency in ${location.name} | Trusted by 100+ Clients`,
    `Expert ${service.name} in ${location.name} | Fast Delivery & Support`,
    `${service.name} Company in ${location.name} | Custom Solutions`,
    `Professional ${service.name} Services in ${location.name} | Get Free Quote`,
  ];
  return templates[h % templates.length];
}

