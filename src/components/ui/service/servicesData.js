// ── GENERATE FULL SEO METADATA ────────────────────────────────
export function generateSEOMetadata() {
  const seoData = {};

  SERVICES.forEach((service) => {
    seoData[service.slug] = {};
    LOCATIONS.forEach((location) => {
      const locationSlug = location.name.toLowerCase().replace(/ /g, "-");
      const canonicalUrl = `https://zwolfconsultancy.com/${service.slug}-in-${locationSlug}.html`;

      seoData[service.slug][locationSlug] = {
        title: getTitle(service, location),
        metaDescription: getMetaDescription(service, location),
        h1: getH1(service, location),
        keywords: getKeywords(service, location),
        canonicalUrl,
        ogTitle: getTitle(service, location),
        ogDescription: getMetaDescription(service, location),
        ogImage: "https://zwolfconsultancy.com/og-image.jpg",
        ogUrl: canonicalUrl,
        twitterTitle: getTitle(service, location),
        twitterDescription: getMetaDescription(service, location),
        schema: getSchema(service, location),
        breadcrumbs: [
          { name: "Home", url: "https://zwolfconsultancy.com/" },
          { name: service.name, url: `https://zwolfconsultancy.com/${service.slug}.html` },
          { name: location.name, url: canonicalUrl }
        ]
      };
    });
  });

  return seoData;
}

// ── HELPER: Get SEO data for a single service + location ──────
export function getSEOData(serviceSlug, locationSlug) {
  const allData = generateSEOMetadata();
  return allData?.[serviceSlug]?.[locationSlug] ?? null;
}

// ── HELPER: Get all location slugs for a service ──────────────
export function getLocationSlugs(serviceSlug) {
  const allData = generateSEOMetadata();
  return Object.keys(allData?.[serviceSlug] ?? {});
}

// ── HELPER: Get all service slugs ─────────────────────────────
export function getServiceSlugs() {
  return SERVICES.map((s) => s.slug);
}

